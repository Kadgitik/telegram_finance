def mcc_to_category(mcc: int) -> str:
    """Map MCC code to a Monobank-style category name."""
    if mcc in _MCC_MAP:
        return _MCC_MAP[mcc]
    # Airlines range 3000-3299 handled above; hotels 3500-3999
    if 3500 <= mcc <= 3999:
        return "Подорожі"
    return "Інше"
